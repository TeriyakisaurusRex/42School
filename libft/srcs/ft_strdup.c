#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t			size;
	char			*ptr;
	unsigned int	i;

	size = ft_strlen(s1);
	ptr = malloc((size + 1) * sizeof(char));
	i = 0;
	if (!ptr)
		return (0);
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
